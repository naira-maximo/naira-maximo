# Seu salário atual é de R$ 6500 reais. Faça um programa que calcule o novo salário com um aumento de 5%
salario_atual = 6500
aumento = (6500*5)/100
salario_com_aumento = salario_atual+aumento
print(f'Seu salario atual é de R${salario_atual:.2f}. Com um aumento de 5% (R${aumento:.2f}), passará a receber R${salario_com_aumento:.2f} como salário.')